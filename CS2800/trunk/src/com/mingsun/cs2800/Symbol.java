package com.mingsun.cs2800;

/**
 * This is the Enum class for all symbol we might use for calculator.
 * 
 * @author Ming Sun <email zaac003@live.rhul.ac.uk>
 * @since 2014-10-12
 */
public enum Symbol {
	/**
	 * @param LEFTBRACKET
	 *            This is "(".
	 */
	LEFTBRACKET,
	/**
	 * @param RIGHTBRACKET
	 *            This is ")".
	 */
	RIGHTBRACKET,
	/**
	 * @param TIME
	 *            This is "x".
	 */
	TIMES,
	/**
	 * @param DIVEDE
	 *            This is "/".
	 */
	DIVIDE,
	/**
	 * @param PLUS
	 *            This is "+".
	 */
	PLUS,
	/**
	 * @param MINUS
	 *            This is "-".
	 */
	MINUS,
	/**
	 * @param INVALID
	 *            This is symbol not above.
	 */
	INVALID;
}
